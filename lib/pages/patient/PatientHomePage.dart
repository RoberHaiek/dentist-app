  Future<void> _handleClinicRegistration(String code) async {
    Navigator.pop(context); // Close dialog

    setState(() => loading = true);

    try {
      // Normalize the entered code â€” trim whitespace, try uppercase first then lowercase
      final normalizedCode = code.trim().toUpperCase();
      final normalizedCodeLower = code.trim().toLowerCase();

      // Try uppercase match (codes generated by app are uppercase)
      var querySnapshot = await FirebaseFirestore.instance
          .collection('clinics')
          .where('registrationCode', isEqualTo: normalizedCode)
          .limit(1)
          .get();

      // Fallback: try exact as-entered (handles manually set lowercase codes like abc123)
      if (querySnapshot.docs.isEmpty) {
        querySnapshot = await FirebaseFirestore.instance
            .collection('clinics')
            .where('registrationCode', isEqualTo: normalizedCodeLower)
            .limit(1)
            .get();
      }

      // Fallback: try exact as typed (neither upper nor lower)
      if (querySnapshot.docs.isEmpty) {
        querySnapshot = await FirebaseFirestore.instance
            .collection('clinics')
            .where('registrationCode', isEqualTo: code.trim())
            .limit(1)
            .get();
      }

      if (querySnapshot.docs.isEmpty) {
        // Code not found
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text(context.tr('invalid_registration_code')),
              backgroundColor: Colors.red,
            ),
          );
          setState(() => loading = false);
        }
        return;
      }

      final clinicDoc = querySnapshot.docs.first;
      final foundClinicId = clinicDoc.id;
      final foundClinicName = clinicDoc.data()['clinicName'];

      final user = FirebaseAuth.instance.currentUser;
      if (user != null) {
        // Update patient document
        await FirebaseFirestore.instance
            .collection('users')
            .doc(user.uid)
            .update({
          'clinicId': foundClinicId,
          'registeredAt': FieldValue.serverTimestamp(),
        });

        // Update clinic document
        await FirebaseFirestore.instance
            .collection('clinics')
            .doc(foundClinicId)
            .update({
          'patients': FieldValue.arrayUnion([user.uid]),
        });

        // Update state
        setState(() {
          clinicId = foundClinicId;
          clinicName = foundClinicName;
          hasClinic = true;
          loading = false;
        });

        // Show success message
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text('${context.tr('connected_to_clinic')}: $foundClinicName'),
              backgroundColor: const Color(0xFF7DD3C0),
              duration: const Duration(seconds: 3),
            ),
          );
        }
      }
    } catch (e) {
      debugPrint('Error registering with clinic: $e');
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('${context.tr('error')}: $e'),
            backgroundColor: Colors.red,
          ),
        );
        setState(() => loading = false);
      }
    }
  }

