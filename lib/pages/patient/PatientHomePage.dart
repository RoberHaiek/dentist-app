  Future<void> _loadPatient() async {
    await currentPatient.loadFromFirestore();

    // Check if patient has a clinic
    final user = FirebaseAuth.instance.currentUser;
    if (user != null) {
      final doc = await FirebaseFirestore.instance
          .collection('users')
          .doc(user.uid)
          .get();

      final data = doc.data();
      clinicId = data?['clinicId'];
      hasClinic = clinicId != null && clinicId!.isNotEmpty;

      // If has clinic, fetch clinic name
      if (hasClinic && clinicId != null) {
        try {
          final clinicDoc = await FirebaseFirestore.instance
              .collection('clinics')
              .doc(clinicId)
              .get();
          clinicName = clinicDoc.data()?['clinicName'];
        } catch (e) {
          debugPrint('Error loading clinic name: $e');
        }
      }

      // NEW: Check appointments
      await _checkAppointments(user.uid);
    }

    setState(() {
      loading = false;
    });

    // Show registration dialog if no clinic
    if (!hasClinic) {
      WidgetsBinding.instance.addPostFrameCallback((_) {
        _showClinicRegistrationDialog();
      });
    }
  }

  // NEW: Check for appointments in next/last 3 days
  Future<void> _checkAppointments(String userId) async {
    try {
      final now = DateTime.now();
      final threeDaysAhead = now.add(const Duration(days: 3));
      final threeDaysAgo = now.subtract(const Duration(days: 3));

      final snapshot = await FirebaseFirestore.instance
          .collection('appointments')
          .where('patientId', isEqualTo: userId)
          .get();

      DateTime? upcoming;
      DateTime? past;

      for (final doc in snapshot.docs) {
        final data = doc.data();
        final type = data['type'] as String? ?? 'appointment';
        if (type != 'appointment') continue;

        final startTime = (data['startTime'] as Timestamp).toDate();
        final status = data['status'] as String? ?? 'confirmed';
        if (status == 'cancelled') continue;

        // Check next 3 days
        if (startTime.isAfter(now) && startTime.isBefore(threeDaysAhead)) {
          if (upcoming == null || startTime.isBefore(upcoming)) {
            upcoming = startTime;
          }
        }

        // Check last 3 days
        if (startTime.isBefore(now) && startTime.isAfter(threeDaysAgo)) {
          if (past == null || startTime.isAfter(past)) {
            past = startTime;
          }
        }
      }

      setState(() {
        nextAppointment = upcoming;
        lastAppointment = past;
      });
    } catch (e) {
      debugPrint('Error checking appointments: $e');
    }
  }

